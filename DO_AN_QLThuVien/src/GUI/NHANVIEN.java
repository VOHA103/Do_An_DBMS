    void loaddulie() {
        modelTB.setRowCount(0);

        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");

        ds = nhanVienDao.getDSNVien();
        for (nhanVien nv : ds) {
            Vector v = new Vector();
            v.add(nv.getMaNV());
            v.add(nv.getHoTen());
            v.add(nv.getGioiTinh());
            v.add(df.format(nv.getNgaySinh()));
            v.add(nv.getSoDT());
            modelTB.addRow(v);
        }
    }

