    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (hoTRo.checkRong(txt_manv) || hoTRo.checkRong(txt_tennv) || hoTRo.checkRong(txt_ngaysinh) || hoTRo.checkRong(txt_manv) || hoTRo.checkRong(txt_sdt) || hoTRo.checkRong(txtPW)) {
            return;

        }
        if (!ktraMaNV(txt_manv.getText())) {
            JOptionPane.showMessageDialog(this, "Ma phong co dang NVxx voi x la chu so", "Thong bao", 1);

            txt_manv.setText("");
            txt_manv.requestFocus();

            return;
        }
        if (!txtPW.getText().equals(txtPrePw.getText())) {
            JOptionPane.showMessageDialog(null, "Password không trùng nhau", "Thông báo", 1);
            txtPrePw.setText("");
            txtPrePw.requestFocus();
            return;
        }
        if (!hoTRo.ktraNgay(txt_ngaysinh) || !hoTRo.ktraSDT(txt_sdt)) {
            return;
        }

        if (JOptionPane.showConfirmDialog(null, "Ban co muon them nhan vien khong" + txt_tennv.getText().trim() + "?", "Thong bao", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {

            String gioiTinh = "Nữ";
            if (rdNam.isSelected()) {
                gioiTinh = "Nam";
            }
            int i = nhanVienDao.themNHanVien(txt_manv.getText().trim(), hoTRo.md5(txtPW.getText().trim()), txt_tennv.getText().trim(), gioiTinh, txt_sdt.getText().trim(), txt_ngaysinh.getText().trim());
            if (i != -1) {
                JOptionPane.showMessageDialog(this, "Ban da them thanh cong", "Thong bao", 1);
                reset();
                loaddulie();
            }
        }

    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed

        int row = tb_qlNhanVien.getSelectedRow();
        if (row >= 0) {

            if (hoTRo.checkRong(txt_manv) || hoTRo.checkRong(txt_tennv) || hoTRo.checkRong(txt_ngaysinh) || hoTRo.checkRong(txt_sdt) || hoTRo.checkRong(txt_sdt)) {
                return;

            }

            if (!ktraMaNV(txt_manv.getText())) {
                JOptionPane.showMessageDialog(this, "Ma phong co dang NVxx voi x la chu so", "Thong bao", 1);

                txt_manv.setText("");
                txt_manv.requestFocus();

                return;
            }
            if (!hoTRo.ktraNgay(txt_ngaysinh) || !hoTRo.ktraSDT(txt_sdt)) {
                return;
            }

            if (JOptionPane.showConfirmDialog(null, "Bạn muốn sửa nhan vien nay  khong", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
                String gioiTinh = "Nữ";
                if (rdNam.isSelected()) {
                    gioiTinh = "Nam";

                }
                int i = nhanVienDao.updateNV(txt_manv.getText().trim(), txt_tennv.getText().trim(), gioiTinh, txt_ngaysinh.getText().trim(), txt_sdt.getText().trim());
                if (i != -1) {
                    JOptionPane.showMessageDialog(null, "Đã sửa thanh cong", "Thông báo", 1);
                    reset();
                    loaddulie();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Mời bạn chọn nhân viên để sửa!!!", "Thông báo", 1);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btn_suaActionPerformed

