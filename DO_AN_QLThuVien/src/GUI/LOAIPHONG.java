    public void loaddulieu() {
        ds = loaiPhongDao.getDSLoai();
        modelTB.setRowCount(0);
        for (loaiPhong p : ds) {
            Vector v = new Vector();
            v.add(p.getMaLoai());
            v.add(p.getTenLoai());
            modelTB.addRow(v);

        }
    }

