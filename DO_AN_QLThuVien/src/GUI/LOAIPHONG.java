    private void tb_dsloaiphongMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_dsloaiphongMouseReleased

        int row = tb_dsloaiphong.getSelectedRow();
        if (row >= 0) {
            txt_maloaiphong.setText((String) (tb_dsloaiphong.getValueAt(row, 0)));
            txt_tenloaiphong.setText((String) (tb_dsloaiphong.getValueAt(row, 1)));
        }
    }//GEN-LAST:event_tb_dsloaiphongMouseReleased
    boolean ktraMaLP(String maPH) {
        String ktra = "LP\\d+";
        if (maPH.matches(ktra)) {
            return true;
        }
        return false;
    }
    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed

        if (hoTRo.checkRong(txt_maloaiphong) || hoTRo.checkRong(txt_tenloaiphong)) {
            return;
        }
        if (!ktraMaLP(txt_maloaiphong.getText())) {
            JOptionPane.showMessageDialog(this, "Ma loai phongco dang LPxx voi x la chu so", "Thong bao", 1);

            txt_maloaiphong.setText("");
            txt_maloaiphong.requestFocus();

            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Ban co muon them khong", "Thong bao", 1) == JOptionPane.YES_NO_OPTION) {
            int i = loaiPhongDao.thenLoaiPhong(txt_maloaiphong.getText().trim(), txt_tenloaiphong.getText().trim());
            if (i != -1) {
                JOptionPane.showMessageDialog(this, "Ban da them thanh cong", "Thong bao", 1);

                loaddulieu();
                reset();
            }
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed

        int row = tb_dsloaiphong.getSelectedRow();
        if (row >= 0) {

            if (hoTRo.checkRong(txt_maloaiphong) || hoTRo.checkRong(txt_tenloaiphong)) {
                return;
            }
            if (!ktraMaLP(txt_maloaiphong.getText())) {
                JOptionPane.showMessageDialog(this, "Ma loai phongco dang LPxx voi x la chu so", "Thong bao", 1);

                txt_maloaiphong.setText("");
                txt_maloaiphong.requestFocus();

                return;
            }
            if (JOptionPane.showConfirmDialog(null, "Bạn muốn sửa  khong", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
                int i = loaiPhongDao.updateLoaiP(txt_maloaiphong.getText(), txt_tenloaiphong.getText());
                if (i != -1) {
                    JOptionPane.showMessageDialog(null, "Đã sửa thanh cong", "Thông báo", 1);

                    loaddulieu();
                }
            }
        }


    }//GEN-LAST:event_btnsuaActionPerformed
