    void loadDuLieu() {
        ds = khachHangDao.getDSKHang();
        modelTB.setRowCount(0);
        for (khachHang kh : ds) {
            Vector v = new Vector();
            v.add(kh.getMaKH());
            v.add(kh.getTenKH());
            v.add(kh.getGioiTinh());
            v.add(kh.getDiaChi());
            v.add(kh.getSoDT());
            modelTB.addRow(v);
        }
    }

