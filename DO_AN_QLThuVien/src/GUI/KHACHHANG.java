    private void btn_luuxuong_txtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuxuong_txtActionPerformed
        String filef = "src//FILE//DS_KhachHang.txt";
        File file = new File(filef);

        try {
            if (JOptionPane.showConfirmDialog(this, "Ban co muon luu vao file txt khong", "Thong bao", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                FileWriter fw = new FileWriter(file);
                BufferedWriter bw = new BufferedWriter(fw);
                JOptionPane.showMessageDialog(this, "Ban da luu file thanh cong", "Thong bao", 2);

                for (int i = 0; i < tb_dskhach.getRowCount(); i++) {
                    for (int j = 0; j < tb_dskhach.getColumnCount(); j++) {

                        bw.write(tb_dskhach.getValueAt(i, j).toString() + "      ");

                    }

                    bw.newLine();
                }

                bw.close();
                fw.close();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_luuxuong_txtActionPerformed

