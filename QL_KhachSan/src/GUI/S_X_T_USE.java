void reset() {
        txt_mkhau.setText("");
        txt_tentk.setText("");
        txt_tentk.requestFocus();
    }
    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (hoTRo.checkRong(txt_tentk) || hoTRo.checkRong(txt_mkhau)) {
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Ban co muon them khong", "Thong bao", 1) == JOptionPane.YES_NO_OPTION) {
            int i = quanTriDao.themTK(txt_tentk.getText(), hoTRo.md5(txt_mkhau.getText()));
            if (i != -1) {
                JOptionPane.showMessageDialog(this, "Ban da them thanh cong", "Thong bao", 1);

                loaddulieu();
                reset();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_themActionPerformed

    private void tb_quantriMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_quantriMouseReleased
        int row = tb_quantri.getSelectedRow();
        if (row >= 0) {
            txt_id.setText((tb_quantri.getValueAt(row, 0).toString()));
            txt_tentk.setText((String) (tb_quantri.getValueAt(row, 1)));
            txt_mkhau.setText((String) (tb_quantri.getValueAt(row, 2)));

        }        // TODO add your handling code here:
    }//GEN-LAST:event_tb_quantriMouseReleased

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed

        if (hoTRo.checkRong(txt_tentk) || hoTRo.checkRong(txt_mkhau)) {
            return;
        }
        int tb = tb_quantri.getSelectedRow();
        if (tb >= 0) {
            if (JOptionPane.showConfirmDialog(null, "Ban co muon xoa khong", "Thong bao", 1) == JOptionPane.YES_OPTION) {
                int i = quanTriDao.xoaTK(txt_id.getText());

                if (i != -1) {
                    JOptionPane.showMessageDialog(null, "Ban da xoa thanh cong", "Thong bao", 1);
                    loaddulieu();
                    reset();
                }
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_xoaActionPerformed

