    public void loaddulieu() {
        ds = quanTriDao.getDSQTri();
        modelTB.setRowCount(0);
        for (quanTri dv : ds) {
            Vector v = new Vector();
            v.add(dv.getId());

            v.add(dv.getTENTK());
            v.add(dv.getMATKHAU());

            modelTB.addRow(v);
        }
    }

