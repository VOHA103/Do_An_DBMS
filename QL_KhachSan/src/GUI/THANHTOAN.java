    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
    
        int row = tb_THANHTOA.getSelectedRow();
        if (row >= 0) {

             if (hoTRo.checkRong(txt_mathue) || hoTRo.checkRong(txt_ngaythanhtoan) || hoTRo.checkRong(txt_thanhtien)) {
            return;
        }
        if (!ktraMaT(txt_mathue.getText())) {
            JOptionPane.showMessageDialog(this, "Ma thue co dang MTxx voi x la chu so", "Thong bao", 1);

            txt_mathue.setText("");
            txt_mathue.requestFocus();

            return;
        }

        if (!hoTRo.kt_tienThue(Integer.parseInt(txt_thanhtien.getText()))) {
            JOptionPane.showMessageDialog(this, "Thanh tien  phải lớn hơn 0", "Thong bao", 1);
            txt_thanhtien.setText("");
            txt_thanhtien.requestFocus();
            return;

        }
        if (!hoTRo.ktraNgay(txt_ngaythanhtoan)) {
            return;
        }

            int i = thanhToanDao.updateSD(txt_mathue.getText().trim(),Float.parseFloat(txt_thanhtien.getText()),txt_ngaythanhtoan.getText().trim());
            if (i != -1) {
                JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", 1);
                reset();
                loadPhong();
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btn_suaActionPerformed
    boolean ktraMaT(String maPH) {
        String ktra = "MT\\d+";
        if (maPH.matches(ktra)) {
            return true;
        }
        return false;
    }

