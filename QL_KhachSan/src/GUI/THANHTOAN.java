    public void loadPhong() {
        dsp = thanhToanDao.getdsTT();
        modelTB.setRowCount(0);
        for (thanhToan p : dsp) {
            Vector v = new Vector();
            v.add(p.getMaThueP());
            v.add(p.getThanhTien());
            v.add(p.getNgayTToan());

            modelTB.addRow(v);
        }
    }

