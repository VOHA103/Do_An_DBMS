    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        btn_xoa.setEnabled(false);
        btn_them.setEnabled(false);

        btn_luu.setEnabled(true);
        btn_huy.setEnabled(true);
        int row = tb_thongtinthuephong .getSelectedRow();
        if (row >= 0) {
        
            if (hoTRo.checkRong(txt_makh) || hoTRo.checkRong(txt_maphong) || hoTRo.checkRong(txt_mathue)
                    || hoTRo.checkRong(txt_ngayra) || hoTRo.checkRong(txt_ngayvao) || hoTRo.checkRong(txt_tienthue)) {
                return;
            }
            if (!hoTRo.ktraNgay(txt_ngayvao) || !hoTRo.ktraSDT(txt_ngayra)) {
                return;
            }
            Date ngayVao, ngayRa;
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayVao = df.parse(txt_ngayvao.getText().trim());
            ngayRa = df.parse(txt_ngayra.getText().trim());
        } catch (Exception e) {
            System.out.println("");
            return;
        }
        System.out.println(ngayVao + " " + ngayRa);
        if (ngayVao.compareTo(ngayRa) >= 0) {
            JOptionPane.showMessageDialog(null, "Ngay muon luon nho hon hoac bang ngay tra", "thong bao", 1);
            return;
        }
            int i = thuePhongDao.updateSD(txt_mathue.getText().trim(), txt_makh.getText().trim(), txt_maphong.getText().trim(), txt_ngayvao.getText().trim(),txt_ngayra.getText().trim(), Integer.parseInt(txt_tienthue.getText()));
            if (i != -1) {
                JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", 1);
                reset();
                loaddata();
            }
        }

    }//GEN-LAST:event_btn_suaActionPerformed

