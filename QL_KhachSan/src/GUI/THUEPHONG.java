    public void loaddata() {
        ds = thuePhongDao.getDSPhong();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");

        modelTB.setRowCount(0);
        for (thuePhong p : ds) {
            Vector v = new Vector();
            v.add(p.getMaThue());
            v.add(p.getMaKH());
            v.add(p.getMaPhong());

            v.add(df.format(p.getNgaVao()));
            v.add(df.format(p.getNgayRa()));
            v.add(p.getTienThueP());

            modelTB.addRow(v);
        }
    }

